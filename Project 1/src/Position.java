public class Position {

    //TODO Comment
    private final int i, j, k;

    public Position(final int i, final int j, final int k){
        this.i = i;
        this.j = j;
        this.k = k;
    }

    public int getI() {
        return i;
    }

    public int getJ() {
        return j;
    }

    public int getK() {
        return k;
    }
}
